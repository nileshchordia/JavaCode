package com.cryptohash;

public class SwitchCaseExample {
    public static void main(String[] args) {
        int x=3;
        switch (x){
            case 1:
                System.out.println("x is 1");
                break;
            case 2:
                System.out.println("x is 2");
                break;
            case 3:
                System.out.println("x is 3");
                break;
                default:
                    System.out.println("No idea about x");
        }
    }
}
